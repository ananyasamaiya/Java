class hi{
    public static void main(String[] args){
        System.out.println("From while loop");
        int a = 1;
        while(a<=10)
        {
            System.out.println(a);
            a++;
        }
        System.out.println("From for loop");
        for(int i = 1;i<=10;i++)
        {
            System.out.println(i);
        }
    }
}